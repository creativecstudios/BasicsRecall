package Bitwise;

public class BitWiseOperator {

    public static void main(String[] args){
        int a = 5;
        int b = 7;

        // AND operators
        System.out.println("a & b = " + (a & b));

        // OR operator
        System.out.println("a | b = " + (a | b));

        // XOR operator
        System.out.println("a ^ b = " + (a ^ b));

        // Compliment operator
    System.out.println("a ~ = " + (~a));
    System.out.println("b ~ = " + (~b));

    // Assignment Operator
    System.out.println("a = " + a);
    System.out.println("b = " + b);
    }
    
}
